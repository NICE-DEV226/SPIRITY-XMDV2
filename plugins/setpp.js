import { downloadMediaMessage } from '@whiskeysockets/baileys';
import Jimp from 'jimp';
import config from '../config.cjs';

const setProfilePicture = async (m, sock) => {
  const botNumber = await sock.decodeJid(sock.user.id);
  const isBot = m.sender === botNumber;
  const prefix = config.PREFIX;
  const cmd = m.body.startsWith(prefix) ? m.body.slice(prefix.length).split(' ')[0].toLowerCase() : '';

  if (cmd !== "setpp") return;

  if (!isBot) {
    return m.reply("‚ùå *Seul le bot peut utiliser cette commande.*");
  }

  if (!m.quoted?.message?.imageMessage) {
    return m.reply("‚ö†Ô∏è *R√©pondez √† une image pour la d√©finir comme photo de profil.*");
  }

  await m.React('‚è≥');

  try {
    let media;
    for (let i = 0; i < 3; i++) {
      try {
        media = await downloadMediaMessage(m.quoted, 'buffer');
        if (media) break;
      } catch {
        if (i === 2) {
          await m.React('‚ùå');
          return m.reply("‚ùå *√âchec du t√©l√©chargement de l'image. R√©essayez.*");
        }
      }
    }

    let image = await Jimp.read(media);
    if (!image) throw new Error("Image invalide");

    const size = Math.max(image.bitmap.width, image.bitmap.height);
    if (image.bitmap.width !== image.bitmap.height) {
      const squareImage = new Jimp(size, size, 0x000000FF);
      squareImage.composite(image, (size - image.bitmap.width) / 2, (size - image.bitmap.height) / 2);
      image = squareImage; // Important : remplacer l'image par la version carr√©e
    }

    image.resize(640, 640);
    const buffer = await image.getBufferAsync(Jimp.MIME_JPEG);

    await sock.updateProfilePicture(botNumber, buffer);
    await m.React('‚úÖ');

    return sock.sendMessage(
      m.from,
      {
        text: `
‚îå‚îÄ„Äî *PHOTO DE PROFIL MISE √Ä JOUR* „Äï‚îÄ‚óâ
‚îÇ
‚îÇ ‚úÖ *Photo de profil mise √† jour avec succ√®s !*
‚îÇ ü§ñ *Bot:* ${botNumber.split("@")[0]}
‚îÇ
‚îî‚îÄ‚û§ *·¥ò·¥è·¥°·¥á Ä·¥á·¥Ö  ô è DARK-DEV*
        `.trim(),
        contextInfo: {
          mentionedJid: [m.sender],
          forwardingScore: 999,
          isForwarded: true,
          forwardedNewsletterMessageInfo: {
            newsletterJid: '120363422353392657@newsletter',
            newsletterName: "DARK-DEV",
            serverMessageId: 143
          }
        }
      },
      { quoted: m }
    );

  } catch (error) {
    console.error("Erreur photo profil:", error);
    await m.React('‚ùå');
    return m.reply("‚ùå *Une erreur est survenue lors de la mise √† jour de la photo de profil.*");
  }
};

